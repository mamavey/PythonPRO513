"""
Тип ошибки 2: Неправильное использование Counter с итерируемыми объектами

Ошибка возникает, когда мы пытаемся создать Counter для данных,
которые не являются итерируемыми объектами, например, словаря или числовых значений.

Пример ошибки:
"""

from collections import Counter

# Ошибка, передаем словарь в Counter
counter = Counter({"a": 2, "b": 3})
print(counter)  # Вывод: Counter({'b': 3, 'a': 2})

"""
Решение:
убедимся, что мы передаём итерируемый объект (например, строку или список) в Counter. 
Если нужно подсчитать частоту для более сложных данных, передаём соответствующую коллекцию.
"""

counter = Counter(["a", "a", "b", "b", "b"])
print(counter)  # Вывод: Counter({'b': 3, 'a': 2})
