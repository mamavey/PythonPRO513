"""
Тип ошибки 3: Обращение к несуществующему ключу в словаре — KeyError

Ошибка возникает, если мы попытаемся получить значение по ключу, которого нет в словаре,
Python выбросит KeyError.

Пример ошибки:
"""

# my_dict = {"name": "Alice", "age": 25}
# print(my_dict["city"])  # Ошибка: ключ 'city' отсутствует

"""
Решение:

Используйте метод .get(), 
который возвращает None (или заданное значение по умолчанию), если ключ отсутствует.
"""

my_dict = {"name": "Alice", "age": 25}
print(my_dict.get("city", "Город не указан"))  # Выведет: Город не указан