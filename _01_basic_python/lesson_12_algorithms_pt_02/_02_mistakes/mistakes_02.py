"""
Тип ошибки 2: Бесконечный цикл в очереди
Ошибка возникает, если при работе с очередью не добавлять условие выхода из цикла.
Программа может застрять в бесконечном цикле.
Пример ошибки:
"""

from collections import deque

# queue = deque([1, 2, 3])
# while queue:  # Неправильное управление очередью
#     print(queue[0])  # Только выводим первый элемент, но не удаляем его
# Программа зависнет

"""
Решение:
Удаляйте элементы из начала очереди:
"""

queue = deque([1, 2, 3])
while queue:
    print(queue.popleft())  # Удаляем элементы по одному