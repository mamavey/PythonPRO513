"""
Тип ошибки 6: Проблемы с доступом к полям класса

Ошибка возникает, когда поле класса используется как поле экземпляра.
Это приводит к созданию локальной копии поля в экземпляре,
вместо изменения общего значения для всех экземпляров.
"""

class Counter:
    count = 0

    def increment(self):
        self.count += 1  # Ошибка: создаётся локальная копия count

counter = Counter()
counter.increment()
print(Counter.count)  # Вывод: 0 (глобальное поле не изменилось)

"""
Решение:

Используйте методы класса, чтобы изменять поля класса.
"""

class Counter:
    count = 0

    @classmethod
    def increment(cls):
        cls.count += 1