"""
Тип ошибки 1: Забытое ключевое слово yield
Ошибка возникает, если вместо yield используется return, функция перестаёт быть генератором.
Пример ошибки:
"""

# def my_generator():
#     return 1  # Ошибка: возвращает только одно значение, генератор не создаётся


"""
Решение:
Используйте yield, чтобы функция стала генератором.
"""


def my_generator():
    yield 1
